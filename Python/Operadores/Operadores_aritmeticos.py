print("***OPERADORES ARITMETICOS***")

a,b= 10, 5
print(f"Valor inicial a:{a}")
print(f"Valor inicial b:{b}")

#Operador de suma +
resultado= a + b
print(f"Resultador operador suma a + b es : {resultado} ")

#Operador de resta -
resultado= a - b
print(f"Resultador operador resta a - b es:{resultado}  ")

#Operador de Multiplicación *
resultado= a * b
print(f"Resultador operador resta a * b es:{resultado}  ")

#Operador de division *
resultado= a / b
print(f"Resultador operador divicion a / b es:{resultado}  ")

#Operador de modulo (residuo de la division)  *
resultado= a % b
print(f"Resultador operador modulo a % b es:{resultado}  ")

#Operador exponente (potencia) *
resultado= 2 ** 3      # base: 2, exponente: 3 = 2*2*2 = 8
print(f"Resultador operador exponente 2 ** 3 es:{resultado} ☺ ")






print("*** Operadores de Asigaci√≥n compuestos***")
a, b =  10, 15
print(f"valor inicial a : {a}")
print(f"valor inicial b : {b}")

# Operador compuesto de suma +=
a += b      # a = a + b
print(f"Resultado operador a += b es: {a} ")

# Operador compuesto de resta -=
a = 10
a -= b    # a = a - b
print(f"resultado operador a -= b es: {a}")

# Operador compuesto de Multiplicacion *=
a = 10
a *= b # a = a * b
print(f"resultado operador a *= b es: {a}")

# Operador compuesto de division /=
a = 10
a /= b    # a = a / b
print(f"resultado operador a /= b es: {a}")
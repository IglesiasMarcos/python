print("*** Operador de comparacion (relacionales) ***")

a, b = 5, 5        # 5, 5 true  5,7 false

print(f"valore inicial de a: {a}")
print(f"valore inicial de b: {b}")

# Operador igualdad == . Regresa un valor logico (True/False)
resultado = a == b
print(f"resultado de a == b es : {resultado}")

# Operador diferente !=
resultado = a != b
print(f"resultado de a != b es: {resultado}")

#Operador mayor que >
resultado = a > b
print(f"resultado de a > b es: {resultado}")

#Operador menor que <
resultado = a < b
print(f"resultado de a < b es: {resultado}")

#Operador mayor o igual que >=
resultado = a >= b
print(f"resultado de a >= b es: {resultado}")

#Operador menor o igual que <=
resultado = a <= b
print(f"resultado de a <= b es: {resultado}")


